package com.qms.rest.model;

public class RefMrssSample {

	private String mrssSampleId;
	private String rate;
	private String sampleSize;
	
	
	public String getMrssSampleId() {
		return mrssSampleId;
	}
	public void setMrssSampleId(String mrssSampleId) {
		this.mrssSampleId = mrssSampleId;
	}
	public String getRate() {
		return rate;
	}
	public void setRate(String rate) {
		this.rate = rate;
	}
	public String getSampleSize() {
		return sampleSize;
	}
	public void setSampleSize(String sampleSize) {
		this.sampleSize = sampleSize;
	}
	
	
}
